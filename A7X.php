			<h5><br><b>Nightmare (2010):</b></h5>
			<p>Premier album apres la disparition de "The Rev", avec Mike Portnoy en batteur temporaire (Dream Theater). La progression de l'album se constitue tout d'abord par quelques chansons
			métal, puissantes. Ensuite quelques chansons un peu plus violentes au travers de laquelle est entrevoutée une pause par l'hommage au Rev, So far Away, qui est chargée en émotions.
			Enfin, Une suite de ballades plutot planantes et mystiques. Le groupe avaient enregistré la voix du Rev avant sa mort, et les a réutilisé dans les chansons. Enfin, The Rev a grandement 
			participé a la création de l'album de par ses travaux de composition.</p>
			
			
			<h5><br><b>Hail to the King (2013):</b></h5>
			<p> C'est concretement le seul album d'A7X sans aucune collaboration du Rev. Le Style musical est réduit a l'essentiel. Lorsqu'on écoute cet album, on reconnait les caractéristiques
			du Heavy Metal, le groupe a préféré aller au coeur du metal, garder le rock au lieu d'essayer de le surcompliquer. C'est également le premier album où le batteur est Arin Llejay, qui est
			officiellement membre du groupe a part entière</p>
