	<h4>Albums enregistrés</h4>
	
		
			<h5><b>1.Sounding the seventh trumped (2001):</b></h5>
			<p> Leur premier album sorti en 2001, album metalcore,très brutal, et dont la violence est stoppée au milieu
			de l'album par la chanson "Warmness on the Soul", débutant par une intro au piano, et une musique clairement moins violente, où Matt shadows y abandonne le scream, pour
			uniquement chanter.</p>
			
			<h5><br><b>Waking the Fallen (2003):</b></h5>
			<p> Le deuxieme album d'A7X, metalcore, mais moins brutal que le premier, principalement par les mélodies de guitares plus recherchées dont les solos de Synyster Gates, et une alternance  Chant/Scream 
			, contrairement au premier album , constitué uniquement de Screaming. De bons titres sont dans cet album, "Chapter Four", "Second Heartbeat", mais également "I won't see you Tonight
			part 1", dont le style musical est assez différent du reste, intégrant des violons, donnant un côté assez épique à cette chanson, qui est un de mes petits coups de coeur.</p>
			
			<h5><br><b>City of the Evil (2005):</b></h5>
			<p>Cet album est le véritable tournant d'Avenged Sevenfold, le style musical change totalement (on reste rock quand même =p), mais est plus accessible au public. En effet, loin des
			cris, cet album est de style Heavy Metal et l'on ressent une forte signature musicale. Les arrangements mélodiques sont plus travaillés, le travail de composition est plus poussé,
			et c'est cet album qui a fait connaitre A7X. On retrouve a la fin de l'album "The Wicked End" et "Strenght of the World" dans lesquel il y a des "pauses" dans le rock, remplacées par 
			des grandes orchestrations, avec entre autre des choeurs et des violons. Ma chanson favorite reste quand même seize the day, une ballade calmant l'energie du reste de l'album, et dont le solo 
			dans le clip officiel est mis en valeur par Synyster Gates jouant sur un cercueil lors d'une cérémonie d'enterrement, qui renforce l'émotion du solo.</p>
			
			<h5><br><b>Avenged Sevenfold (2007):</b></h5>
			<p>Cet album est dans la continuation de City of the Evil. Ce qui diffère dans l'album eponyme est la présence de style différents sur chaque album, 
			on dénote la présence de pseudo-rap dans "Critical Acclaim" qui entame l'album magistralement par de l'orgue, et de reflets country, notamment 
			dans "Gunsliner" et "Dear God" et une grande piece musicale, "A little piece of heaven"* qui raconte une histoire d'amour belle mais sordide. (*cf : Player Youtube)</p>
	
